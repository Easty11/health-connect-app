import { mkdirSync, writeFileSync } from 'node:fs';

const SPEC = 'https://health-app-backend-production-760e.up.railway.app/openapi.json';

const spec = await (await fetch(SPEC)).json();
const sst = spec.components?.schemas?.SleepStageType;
const names = sst?.['x-enum-varnames'];
const values = sst?.enum;
if (!names || !values || names.length !== values.length) {
  throw new Error('SleepStageType missing x-enum-varnames/enum — is the backend deployed?');
}
const body = names.map((n, i) => `  ${n}: ${values[i]},`).join('\n');
const out = `// AUTO-GENERATED by \`npm run gen:contract\` from /openapi.json — DO NOT EDIT.
// Source of truth: backend SleepStageType (routers/health_connect.py).
export const SleepStageType = Object.freeze({
${body}
});
`;
mkdirSync(new URL('../src/contract/', import.meta.url), { recursive: true });
writeFileSync(new URL('../src/contract/sleepStages.generated.js', import.meta.url), out);
console.log('Wrote src/contract/sleepStages.generated.js:', names.join(', '));
